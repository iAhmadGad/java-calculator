package script;

public class MathOperations {
	
	//addition operation
	public int add(int onum1, int onum2)
	{
		
		int oresult = onum1 + onum2;
		return oresult;
		
	}
	
	//subtract operation
	public int sub(int onum1, int onum2)
	{
		
		int oresult = onum1 - onum2;
		return oresult;
		
	}
	
	//multiply operation
	public int mtp(int onum1, int onum2)
	{
		
		int oresult = onum1 * onum2;
		return oresult;
		
	}
	
	//divide operation
	public int dvd(int onum1, int onum2)
	{
		
		int oresult = onum1 / onum2;
		return oresult;
		
	}
	
}
